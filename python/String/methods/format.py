text1 = "My name is {} and I am {} years old.".format("Ali", 25)
print(text1)  # Output: "My name is Ali and I am 25 years old."

text2 = "My favorite language is {lang} and my age is {age}.".format(lang="Python", age=30)
print(text2)  # Output: "My favorite language is Python and my age is 30."

price = "The product costs ${:.2f}".format(9.4567)
print(price)  # Output: "The product costs $9.46"
